package dataTypes_operators;

public class Switch_Demo {

	public static void main(String[] args) {
		
		/* Switch Statement Syntax:
		 * 
		 * switch (expression){
		 * case value1:
		 * //code to be executed
		 * break;
		 * case value2:
		 * // code to be executed
		 * break;
		 * case value3:
		 * // code to be executed
		 * break;
		 * 
		 * default:
		 * // code to be executed
		 * }
		 */
		int month = 5;
		switch(month) {
		case 1: System.out.println("January is my ammu's birth month");
		break;
		case 2: System.out.println("February is my wife birth month"); 
		break;
		case 3: System.out.println("March");
		break;
		case 4: System.out.println("April");
		break;
		case 5: System.out.println("May is the month of my bith");
		break;
		case 6: System.out.println("June");
		break;
		case 7: System.out.println("July is my abbu's birth month");
		break;
		case 8: System.out.println("August");
		break;
		case 9: System.out.println("September");
		break;
		case 10: System.out.println("October");
		break;
		case 11: System.out.println("November");
		break;
		case 12: System.out.println("December");
		break;
		default: System.out.println("This is not a month name");
		
		}
		
		
		
		
		
		

	}

}
