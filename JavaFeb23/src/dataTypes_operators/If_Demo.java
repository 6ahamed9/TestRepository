package dataTypes_operators;

public class If_Demo {

	public static void main(String[] args) {
		
		/**
		 * Syntax:
		 * if (boolean condition)
		 * {
		 * Code to be executed
		 * }
		 */
		
		// This is an example of control statement
		int age = 16;
		System.out.println("Testing if condition status: ");
		
		if (age>18) // if the condition is false java will skip the code
		{
			System.out.println("You are eligivle to marry !!");
		}

		System.out.println("Testing if condition ends !!!");
		
		
		
		
		

	}

}
