package dataTypes_operators;

public class For_Loop {

	public static void main(String[] args) {
		
		/**
		 *  for (initialization/start point, end point/how far, how will it increase/decrease)
		 *  {
		 *  code to be executed
		 *  }
		 */
		
		// Write "I love you" 10 times using for loop
		for (int i=1; i<=10; i++) {
			System.out.println("I love you");
		}
		
		
		
		

	}

}
